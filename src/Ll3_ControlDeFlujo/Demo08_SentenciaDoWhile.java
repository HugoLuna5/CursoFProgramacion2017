public class Demo08_SentenciaDoWhile {
    public static void main(String[] args) {
        int i; //Declarar variable
        i = 1; //Inicialización de la variable
        
        do{//Sentencias
            System.out.println(i);//Imprimir i

            i++;//i = i +1; Incremento
        }while (i <= 10);//Expresión condicional del ciclo do while
        //Fin del DoWhile
    }
}
