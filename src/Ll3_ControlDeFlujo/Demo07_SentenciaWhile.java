public class Demo07_SentenciaWhile {

    public static void main(String[] args) {
        int i; //Declarar variable
        i = 1; //Inicialización de la variable
        while (i <= 10) {//Expresión condicional del ciclo while
            System.out.println(i);//Imprimir i

            i++;//i = i +1; Incremento
        }//Fin while
    }
}
